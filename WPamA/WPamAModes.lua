WPamA.ModeCount = #WPamA.ModeSettings + 1
WPamA.ModeOrder = {}
do
  local nOrd = 0
  local modeSet = WPamA.ModeSettings
  for i = 0, WPamA.WindCount - 1 do
    local v = WPamA.WindSettings[i]
    if v then
      for j = 1, WPamA.TabBCount do
        local q = v.Mds[j]
        if q and modeSet[q] ~= nil then
          modeSet[q].WinInd = i
          modeSet[q].TabInd = j
          modeSet[q].MOrder = nOrd
          WPamA.ModeOrder[nOrd] = q
          nOrd = nOrd + 1
        end
      end
    end
  end
  ---
  local maxX = WPamA.Consts.WinSizeMinX
  for i = 0, #modeSet do
    local ms = modeSet[i]
    if ms and ms.WinX and (maxX < ms.WinX) then maxX = ms.WinX end
  end
  WPamA.Consts.WinSizeMaxX = maxX
  ---
end
