function JournalQuestLog.GetIsMissable(questId)
	local quest = missableQuests[questId]
	local isMissable
	if quest == nil then isMissable = JQL_MISSABLE_STATE_NOT_MISSABLE
	else
		isMissable = JQL_MISSABLE_STATE_MISSABLE
		for i = 1,#quest do
			local questInfo = GetCompletedQuestInfo(quest[i])
			if questInfo ~= "" then isMissable = JQL_MISSABLE_STATE_MISSED
			end
		end
	end
	return isMissable
end