function JournalQuestLog.GetSkipCyrodiil(questId)
	local myAlliance = GetUnitAlliance("player")
	local skipCyrodiil = false
	for _,v in pairs(cyrodiilQuests[myAlliance]) do
		if v == questId then
			skipCyrodiil = true
			break
		end
	end
	return skipCyrodiil
end






