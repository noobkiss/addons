local lower = string.lower

local function utf8_codes(s)
    local i = 1
    local len = #s

    return function()
        if i > len then return nil end

        local byte = s:byte(i)
        local n

        if byte < 0x80 then
            n = 1
        elseif byte < 0xC0 then
            error('Invalid UTF-8 continuation byte at position ' .. i)
        elseif byte < 0xE0 then
            n = 2
        elseif byte < 0xF0 then
            n = 3
        elseif byte < 0xF8 then
            n = 4
        else
            error('Invalid UTF-8 leading byte at position ' .. i)
        end

        local codepoint_str = s:sub(i, i + n - 1)
        i = i + n

        return codepoint_str
    end
end

