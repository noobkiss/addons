local seen = {}
local missing = {}
local function mark(cp)
    seen[cp] = true
    if not MAP[cp] then
        missing[cp] = true
    end
end

local function normalize(s)
    local result = {}

    for cp in utf8_codes(s) do
        if #cp == 1 then
            result[#result+1] = lower(cp)
        else
            mark(cp)
            result[#result+1] = MAP[cp] or cp
        end
    end

    return table.concat(result)
end

IMP_NORMALIZE_NAMES = {
    __missing = missing,
    __seen = seen,
    Normalize = normalize,
}
